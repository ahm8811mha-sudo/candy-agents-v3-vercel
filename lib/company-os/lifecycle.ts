export function getLifecycleStage(id: LifecycleStage["id"]) {
  return COMPANY_LIFECYCLE.find((stage) => stage.id === id);
}

export function validateLifecycle() {
  const orders = COMPANY_LIFECYCLE.map((stage) => stage.order);
  const uniqueOrders = new Set(orders);
  return {
    valid: COMPANY_LIFECYCLE.length === 10 && uniqueOrders.size === 10 && Math.min(...orders) === 1 && Math.max(...orders) === 10,
    stageCount: COMPANY_LIFECYCLE.length,
  };
}
